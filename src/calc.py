def add(a, b):
    """Return sum of two positive integers."""
    return a + b

def subtract(a, b):
    """Return difference of two positive integers."""
    return a - b

def multiply(a, b):
    """Return product of two positive integers."""
    return a * b

def divide(a, b):
    """Return division of two positive integers."""
    return a / b  # Note: float result

def modulus(a, b):
    """Return remainder of division of two positive integers."""
    return a % b

def power(a, b):
    """Return a raised to the power of b."""
    return a ** b
